package programmers


/**
 * 배열의 평균값
 * - [link](https://school.programmers.co.kr/learn/courses/30/lessons/120817)
 * - 정수 배열 numbers의 원소의 평균값을 return
 */
class P120817 {
    companion object {
        // 정수배열의 평균값
        fun solution(numbers: IntArray): Double {
            return numbers.average()
        }
    }
}
